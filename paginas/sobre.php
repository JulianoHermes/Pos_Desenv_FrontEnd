	<h1>Sobre o Burnes King</h1>

	<p>O Burnes King do Brasil é um master franqueado do Burnes King Corporation no país. Nossa marca está aqui desde 1990, quando inauguramos nosso primeiro restaurante no Shopping Xing Ling, mas foi em 2011, após o Burnes King Corporation ser adquirido pelo grupo 4G capital, que iniciamos a nossa operação própria aqui no país, através de uma joint venture entre DaVinci Partners e 4G Capital.</p>
	 
	<p>De 2011 para cá já crescemos muito, e nossos números comprovam isso. Somos uma empresa que em meados de 2011 possuía alguns colaboradores em Xanxere distribuídos entre 17 pontos de venda. Hoje, já somamos mais de 6 mil talentos espalhados em 12 estados brasileiros e atingiremos, até o final deste ano, 480 pontos de venda.</p>
	 
	<p>Acreditamos que o nosso bem mais valioso é a nossa Gente. Por isso, apostamos na carreira de nossos colaboradores, com o objetivo de impulsionar a visão de dono daqueles que, junto com a gente, acreditam em um Sonho Grande: ser a mais prazerosa experiência de alimentação.</p>
	 
	<p>Para sustentar esse sonho, nos focamos em quatro pilares: sermos a marca de fast food preferida, mais rentável, com pessoas talentosas e com forte presença nacional.</p>

